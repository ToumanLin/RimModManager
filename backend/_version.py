def get_all_changelogs(include_unreleased=None):
    """
    获取结构化更新日志给前端。
    `include_unreleased` 为 None 时，根据当前 build 类型决定是否展示未发布版本。
    """
    if include_unreleased is None:
        include_unreleased = str(__build__ or "").strip().lower() == "dev"
    if include_unreleased:
        return APP_CHANGELOG
    return [item for item in APP_CHANGELOG if item.get("date")]
